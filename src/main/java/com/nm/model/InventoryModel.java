package com.nm.model;

public class InventoryModel {
	
	public String name;
	
	public String authorDetails;
	
	public String printDisabled;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAuthorDetails() {
		return authorDetails;
	}

	public void setAuthorDetails(String authorDetails) {
		this.authorDetails = authorDetails;
	}

	public String getPrintDisabled() {
		return printDisabled;
	}

	public void setPrintDisabled(String printDisabled) {
		this.printDisabled = printDisabled;
	}  

}
